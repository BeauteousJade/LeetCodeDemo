    /**
     * BFS。
     * <p>
     * 注意：计算最短路的时候，入队的条件应该用距离来判断。
     */
    public String findShortestWay(int[][] maze, int[] ball, int[] hole) {
        int[][] distance = new int[maze.length][maze[0].length];
        String[][] paths = new String[maze.length][maze[0].length];
        for (int i = 0; i < distance.length; i++) {
            Arrays.fill(distance[i], Integer.MAX_VALUE);
            Arrays.fill(paths[i], "");
        }
        int[][] dirs = {{1, 0}, {0, -1}, {0, 1}, {-1, 0}};
        String[] ps = {"d", "l", "r", "u"};
        Queue<int[]> queue = new LinkedList<>();
        queue.offer(new int[]{ball[0], ball[1]});
        distance[ball[0]][ball[1]] = 0;
        paths[ball[0]][ball[1]] = "";
        while (!queue.isEmpty()) {
            int[] node = queue.remove();
            for (int i = 0; i < dirs.length; i++) {
                int[] dir = dirs[i];
                String p = ps[i];
                int count = 0;
                int x = node[0] + dir[0];
                int y = node[1] + dir[1];
                // 朝一个方向走，直到边界。
                while (x >= 0 && x < maze.length && y >= 0 && y < maze[0].length && maze[x][y] == 0) {
                    if (x == hole[0] && y == hole[1]) {
                        x += dir[0];
                        y += dir[1];
                        count++;
                        break;
                    }
                    x += dir[0];
                    y += dir[1];
                    count++;
                }
                x -= dir[0];
                y -= dir[1];
                int newDistance = distance[node[0]][node[1]] + count;
                // 这里需要判断两种情况：
                // 1. 最短路径。
                // 2. 在相同路径下，字典序最小。
                if (newDistance < distance[x][y] || (newDistance == distance[x][y] && ((paths[node[0]][node[1]] + p).compareTo(paths[x][y]) < 0))) {
                    distance[x][y] = newDistance;
                    paths[x][y] = paths[node[0]][node[1]] + p;
                    queue.offer(new int[]{x, y});
                }
            }
        }
        return paths[hole[0]][hole[1]].equals("") ? "impossible" : paths[hole[0]][hole[1]];
    }

